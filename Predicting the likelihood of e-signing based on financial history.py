# Predicting the likelihood of E-Signing a Loan based on financial history

# Lending companies work by analyzing the financial history of their loan applicants, and choosing whether or not the applicant is
# too risky to be given a loan. If the applicant is not, the company then determines the terms of the loan. To acquire these 
# applicants, companies can organically receive them through their websites/apps, often with the help of advertisement campaigns.
# Other times, lending companies partner with peer-to-peer (P2P) lending marketplaces, in order to acquire leads of possible 
# applicants. 
# 
# Market: The target audience is the set of loan applicants who reached out through an intermediary marketplace.
#     
# Product: A loan
# 
# Goal: Develop a model to predict for 'quality' applicants. In this case study, 'quality' applicants are those who reach a key 
# part of the loan application process.
#     

# Here, the goal is to create a model that predicts whether or not the applicants will complete the electronic signature phase
# of the loan application (e_signed). 

# The official application begins with the lead arriving into our website after we opted to acquire it. Here, the applicant begins 
# the onboarding process to apply for a loan. The user begins to provide more financial information by going over screen of the 
# onboarding process. This first phase ends with the applicant providing his/her signature indicating all of the given information
# is correct.

